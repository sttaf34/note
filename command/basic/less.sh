# -N -> 行番号表示
history | less -N

#
# less に入ってから
#

# 基本
# 1行進む     -> e or ENTER
# 1行戻る     -> y
# 1ページ進む -> f
# 1ページ戻る -> b
# 終了        -> q

# いろいろ
# 一番最初に移動 -> g or <
# 一番最後に移動 -> G or >
# 検索           -> / (vimでも一緒)
# 次の検索結果へ -> n (vimでも一緒)

